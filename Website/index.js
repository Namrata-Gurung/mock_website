//toggle the hamburger menu when clicked
function openMenu(x) {
  x.classList.toggle("change")
  document.getElementById("navigation-overlay").style.display ="flex";
}
function closeMenu(x){
    x.classList.toggle("change")
    document.getElementsById("overlay-links").style.display = "none";
}

